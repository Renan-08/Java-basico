public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        // Adição
        int soma = a + b;
        System.out.println("Adição: " + soma); // 10 + 3 = 13

        // Subtração
        int subtracao = a - b;
        System.out.println("Subtração: " + subtracao); // 10 - 3 = 7

        // Multiplicação
        int multiplicacao = a * b;
        System.out.println("Multiplicação: " + multiplicacao); // 10 * 3 = 30

        // Divisão
        int divisao = a / b;
        System.out.println("Divisão: " + divisao); // 10 / 3 = 3 (divisão inteira)

        // Módulo
        int modulo = a % b;
        System.out.println("Módulo: " + modulo); // 10 % 3 = 1 (resto da divisão)
    }
}

